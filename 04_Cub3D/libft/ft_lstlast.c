#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	if (lst == NULL)
		return (NULL);
	while (lst->next != NULL)
		lst = lst -> next;
	return (lst);
}

/*Returns the last node in the list.
	lst parameters: the beginning of the list.
	Return value: Last node in the list.*/
